package com.xworkz.hostel.office;

public class Office {
	
	private float openTime;
	private float closeTime;
	private int lunchBrake;
	private String systemUsedInCompany;
	private String printer;
	
	public float getOpenTime() {
		return openTime;
	}
	public void setOpenTime(float openTime) {
		this.openTime = openTime;
	}
	public float getCloseTime() {
		return closeTime;
	}
	public void setCloseTime(float closeTime) {
		this.closeTime = closeTime;
	}
	public int getLunchBrake() {
		return lunchBrake;
	}
	public void setLunchBrake(int lunchBrake) {
		this.lunchBrake = lunchBrake;
	}
	public String getSystemUsedInCompany() {
		return systemUsedInCompany;
	}
	public void setSystemUsedInCompany(String systemUsedInCompany) {
		this.systemUsedInCompany = systemUsedInCompany;
	}
	public String getPrinter() {
		return printer;
	}
	public void setPrinter(String printer) {
		this.printer = printer;
	}
	
	
}
